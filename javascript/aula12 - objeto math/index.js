let numero = 50.13;
console.log(numero);

// Math.ceil arredonda sempre para cima
console.log(`Math.ceil(${numero}) = ${Math.ceil(numero)}`);

// Math.floor arredonda sempre para baixo
console.log(`Math.floor(${numero}) = ${Math.floor(numero)}`);

console.log(`Math.round(${numero}) = ${Math.round(numero)}`);

// Math.max
console.log(Math.max(34, 67, 89, 21, 66, 98, 112, 1, 3, 124));

// Math.min
console.log(Math.min(34, 67, 89, 21, 66, 98, 112, 1, 3, 124, -3));

// Math.pow()
console.log(Math.pow(8, 3));
console.log(8**3);

// Math.random()
console.log(Math.random());

// Math.sqrt()
console.log(Math.sqrt(25));

// Math.cbrt()
console.log(Math.cbrt(27));

